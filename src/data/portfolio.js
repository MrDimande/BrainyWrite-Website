export const getPortfolioByCategory = (category) => {
  return portfolioItems.filter(item => item.category === category)
}

export const getPortfolioStats = () => {
  const total = portfolioItems.length
  const completed = portfolioItems.filter(item => item.status === 'Concluído').length
  const inProgress = portfolioItems.filter(item => item.status === 'Em Desenvolvimento').length
  const submitted = portfolioItems.filter(item => item.status === 'Submetido').length
  
  return {
    total,
    completed,
    inProgress,
    submitted,
    completionRate: Math.round((completed / total) * 100)
  }
}